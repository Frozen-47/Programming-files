#include <iostream>
using namespace std;
int main()
{
    int i;
    float f;
    double d;
    char c;
    bool b;
    cout<<"Integer size : "<<sizeof(i)<<endl;
    cout<<"Float size   : "<<sizeof(f)<<endl;
    cout<<"Double size  : "<<sizeof(d)<<endl;
    cout<<"Char size    : "<<sizeof(c)<<endl;
    cout<<"Bool size    : "<<sizeof(b)<<endl;
    return 0;
}